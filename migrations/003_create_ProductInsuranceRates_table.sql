    FloodCoverage varchar(25) NULL,
    EarthquakeCoverage varchar(25) NULL,
    Umbrellacoverage varchar(25) NULL,
    GeneralLiabilityRate numeric(19,2) NULL,
    AdditionalLiabilityRatio real NULL,
    TRIARate real NULL,
    TaxRate real NULL,
    CompanyFee numeric(19,2) NULL,
    BrokerFee numeric(19,2) NULL,
    PropertyRate1 real NULL,
    PropertyRate2 real NULL,
    CreateUserID integer NULL,
    ModifyUserID integer NULL,
    CreateDate timestamp NOT NULL DEFAULT NOW(),
    ModifyDate timestamp NULL,
