    RateStatusReason varchar(50) NULL,
    InsuranceCompany varchar(100) NULL,
    InsuranceCompanyNAIC varchar(10) NULL,
    DwellingAmountInTiv boolean NOT NULL,
    AdditionalBuildingAmountInTiv boolean NOT NULL,
